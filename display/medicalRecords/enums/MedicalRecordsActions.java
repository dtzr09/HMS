package display.medicalRecords.enums;

/**
 * The {@code MedicalRecordsActions} enum represents the possible actions
 * that can be performed on a patient's medical records in the Hospital
 * Management System.
 */
public enum MedicalRecordsActions {
    /** Represents the action of viewing medical records. */
    VIEW,

    /** Represents the action of updating medical records. */
    UPDATE
}
